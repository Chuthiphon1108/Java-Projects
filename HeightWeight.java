    private void btnSumActionPerformed(java.awt.event.ActionEvent evt) {                                       
        try {
            double w = Double.parseDouble(txtWeight.getText());
            double h = Double.parseDouble(txtHeight.getText());
            h = h / 100;
            double sum = w / (h * h);

            if (sum < 19) {
                txtSum.setOpaque(true);
                txtSum.setBackground(Color.green);
                txtSum.setText("ปกติ");

            } else if (sum < 24) {
                txtSum.setOpaque(true);
                txtSum.setBackground(Color.yellow);
                txtSum.setText("ท้วม");

            } else if (sum < 26) {
                txtSum.setOpaque(true);
                txtSum.setBackground(Color.pink);
                txtSum.setText("อ้วนระดับที่1");

            } else {
                txtSum.setOpaque(true);
                txtSum.setBackground(Color.red);
                txtSum.setText("อ้วนระดับที่2");

            }
        } catch (NumberFormatException e) {

            JOptionPane.showMessageDialog(null, "กรุณาใส่ตัวเลขที่ถูกต้อง");
        }
    }                                      

